    private void jButtonLihatLINKActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonLihatLINKActionPerformed
        int r = jTable1.getSelectedRow();
        if (r < 0) {
            JOptionPane.showMessageDialog(this, "Pilih baris di tabel.");
            return;
        }

        String link = String.valueOf(jTable1.getValueAt(r, 0)).trim();

        if (link.isEmpty() || link.equalsIgnoreCase("null")) {
            JOptionPane.showMessageDialog(this, "Baris ini belum memiliki link.");
            return;
        }

        if (!(link.startsWith("http://") || link.startsWith("https://"))) {
            link = "https://" + link;
        }

        try {
            java.net.URI uri = new java.net.URI(link);
            if (!java.awt.Desktop.isDesktopSupported()) {
                JOptionPane.showMessageDialog(this, "Desktop tidak mendukung buka browser otomatis.");
                return;
            }
            java.awt.Desktop.getDesktop().browse(uri);

        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this,
                "Link tidak valid: " + link,
                "Error",
                JOptionPane.ERROR_MESSAGE
            );
        }
    }//GEN-LAST:event_jButtonLihatLINKActionPerformed

