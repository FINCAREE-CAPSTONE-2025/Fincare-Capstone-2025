    private void jButtonOKEActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonOKEActionPerformed
        String link = jTextFieldLINKSertif.getText().trim();
        if (link.isEmpty()) {
            javax.swing.JOptionPane.showMessageDialog(this, "Masukkan link sertifikat.");
            return;
        }
        if (!(link.startsWith("http://") || link.startsWith("https://"))) {
            link = "https://" + link;
        }

        try {
            int sel = jTable1.getSelectedRow();

            if (sel >= 0) {
                String st = String.valueOf(tblModel.getValueAt(sel, 1));
                if ("DITOLAK".equalsIgnoreCase(st)) {
                    Integer id = rowIds.get(sel);
                    sertifSvc.resubmitLinkDitolak(id, link);
                    javax.swing.JOptionPane.showMessageDialog(this, "Link dikirim ulang. Status menjadi MENUNGGU.");
                    jTextFieldLINKSertif.setText("");
                    muatData();
                    return;
                }
            }
            
            if (sertifSvc.hasPendingDariNik(penggunaLogin.getNik())) {
                javax.swing.JOptionPane.showMessageDialog(this,
                    "Masih ada sertifikat MENUNGGU. Tunggu verifikasi selesai sebelum upload lagi.");
                return;
            }

            PelItem item = (PelItem) cmbPelatihan.getSelectedItem();
            if (item == null || item.id == null) {
                sertifSvc.tambahLinkSertifikat(penggunaLogin.getNik(), link);
            } else {
                sertifSvc.tambahLinkSertifikatDariPelatihan(penggunaLogin.getNik(), item.id, link);
            }
            javax.swing.JOptionPane.showMessageDialog(this, "Link sertifikat di-upload. Menunggu verifikasi.");
            jTextFieldLINKSertif.setText("");
            muatData();

        } catch (Exception ex) {
            logger.log(java.util.logging.Level.SEVERE, "Upload/Kirim ulang link", ex);
            javax.swing.JOptionPane.showMessageDialog(this, ex.getMessage(), "Error",
                    javax.swing.JOptionPane.ERROR_MESSAGE);
        }

    }//GEN-LAST:event_jButtonOKEActionPerformed

