    private void initTable() {
        tblModel = new DefaultTableModel(
            new Object[]{"Link Sertifikat", "Status", "Tanggal Upload"}, 0
        ){
            @Override 
            public boolean isCellEditable(int r, int c) { 
                return false; 
            }
        };

        jTable1.setModel(tblModel);
        jTable1.getTableHeader().setReorderingAllowed(false);
    }

    private void muatData() {
        tblModel.setRowCount(0);
        rowIds.clear();

        var rows = sertifSvc.daftarSertifikatPengguna(penggunaLogin.getNik());
        for (Object[] r : rows) {
            Integer id   = (Integer) r[0];
            String  link = (String)  r[2];
            String  stt  = (String)  r[3];
            Object  tglO = r[4];

            String tglStr = "-";
            if (tglO instanceof java.time.LocalDate d) tglStr = d.toString();
            else if (tglO != null) tglStr = String.valueOf(tglO);

            rowIds.add(id);
            tblModel.addRow(new Object[]{ link == null ? "" : link, stt, tglStr });
        }

        onRowSelected();
    }

    private void pasangRowListener() {
        jTable1.getSelectionModel().addListSelectionListener((e) -> {
            if (e.getValueIsAdjusting()) return;
            onRowSelected();
        });
    }

    private void onRowSelected() {
        int r = jTable1.getSelectedRow();
        if (r < 0) {
            // tetap bisa upload baru
            jButtonOKE.setEnabled(true);
            jTextFieldLINKSertif.setEditable(true);
            jTextFieldLINKSertif.setText("");
            return;
        }
        String status = String.valueOf(tblModel.getValueAt(r, 1)); // Status
        String link   = String.valueOf(tblModel.getValueAt(r, 0)); // Link
        jTextFieldLINKSertif.setText("null".equalsIgnoreCase(link) ? "" : link);

        // Selalu boleh mengetik; kalau status DITOLAK = kirim ulang, selain itu = upload baru
        jButtonOKE.setEnabled(true);
        jTextFieldLINKSertif.setEditable(true);
    }


    private void simpanLink() {
        String link = jTextFieldLINKSertif.getText().trim();
        if (link.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Masukkan link sertifikat terlebih dahulu.");
            return;
        }
        if (!(link.startsWith("http://") || link.startsWith("https://"))) {
            int ok = JOptionPane.showConfirmDialog(
                this, "Link tidak diawali http/https. Lanjutkan simpan?", 
                "Konfirmasi", JOptionPane.YES_NO_OPTION
            );
            if (ok != JOptionPane.YES_OPTION) return;
        }

        try {
            PelItem item = (PelItem) cmbPelatihan.getSelectedItem();
            if (item == null || item.id == null) {

                sertifSvc.tambahLinkSertifikat(penggunaLogin.getNik(), link);
            } else {

                sertifSvc.tambahLinkSertifikatDariPelatihan(penggunaLogin.getNik(), item.id, link);
            }
            JOptionPane.showMessageDialog(this, "Link sertifikat tersimpan.");
            jTextFieldLINKSertif.setText("");
            muatData();
        } catch (Exception ex) {
            logger.log(java.util.logging.Level.SEVERE, "Gagal simpan link", ex);
            JOptionPane.showMessageDialog(this, ex.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
    }


    private void muatComboPelatihan() {
        var model = new javax.swing.DefaultComboBoxModel<PelItem>();
        model.addElement(new PelItem(null, "Tidak terkait pelatihan")); // item 0

        try {
            var list = pelatihanSvc.getRiwayatPelatihanDariNik(penggunaLogin.getNik());
            for (Object[] r : list) {
                
                Integer id = null;
                Object idObj = r[0];
                if (idObj instanceof Number n) id = n.intValue();
                else if (idObj != null) id = Integer.valueOf(idObj.toString());

                String judul   = (String)  r[1];
                String lembaga = (String)  r[2];

                String label = (judul == null ? "(Tanpa judul)" : judul) +
                               " â€“ " + (lembaga == null ? "" : lembaga);
                model.addElement(new PelItem(id, label));
            }
        } catch (Exception ex) {
            logger.log(java.util.logging.Level.SEVERE, "Load combo pelatihan", ex);
        }

        cmbPelatihan.setModel(model);
        cmbPelatihan.setSelectedIndex(0);
    }

