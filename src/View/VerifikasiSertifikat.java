    private void jButtonKlikLinkActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonKlikLinkActionPerformed
        int r = jTableSertifikat.getSelectedRow();
        if (r < 0) {
            JOptionPane.showMessageDialog(this, "Pilih baris di tabel terlebih dahulu.");
            return;
        }

        int linkCol = jTableSertifikat.getColumnModel().getColumnIndex("Link");
        String link = String.valueOf(tblModel.getValueAt(r, linkCol));

        if (link == null || link.isBlank() || "null".equalsIgnoreCase(link)) {
            JOptionPane.showMessageDialog(this, "Tidak ada link sertifikat pada baris ini.");
            return;
        }

        if (!(link.startsWith("http://") || link.startsWith("https://"))) {
            link = "https://" + link.trim();
        }

        try {
            java.awt.Desktop.getDesktop().browse(new java.net.URI(link));
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, "Gagal membuka link: " + ex.getMessage());
        }
    }//GEN-LAST:event_jButtonKlikLinkActionPerformed
    
    private void initTable() {
        tblModel = new javax.swing.table.DefaultTableModel(
                    new Object[]{"ID","NIK","Nama", "ID Pelatihan", "Status","Link","Tanggal Upload"}, 0
        ){
            @Override 
            public boolean isCellEditable(int r,int c){ 
                return false; 
            }
        };
        jTableSertifikat.setModel(tblModel);
        jTableSertifikat.getTableHeader().setReorderingAllowed(false);

        hideColumn(1);

        jTableSertifikat.getSelectionModel().addListSelectionListener(e -> {
            if (!e.getValueIsAdjusting()) onRowSelected();
        });

        jTableSertifikat.addMouseListener(new java.awt.event.MouseAdapter() {
            @Override 
            public void mouseClicked(java.awt.event.MouseEvent e) {
                if (e.getClickCount() == 2) {
                    int r = jTableSertifikat.rowAtPoint(e.getPoint());
                    if (r >= 0) {
                        String link = String.valueOf(tblModel.getValueAt(r, 4));
                        bukaLink(link);
                    }
                }
            }
        });
    }

    private void onRowSelected() {
        int r = jTableSertifikat.getSelectedRow();
        if (r < 0) {
            selectedId = null;
            linkTerpilih = "";
            jButtonKlikLink.setText("KLIK LINK DISINI");
            jButtonDiterima.setEnabled(false);
            jButtonDitolak.setEnabled(false);
            return;
        }
        selectedId   = (Integer) tblModel.getValueAt(r, 0);
        String status= String.valueOf(tblModel.getValueAt(r, 3));
        linkTerpilih = String.valueOf(tblModel.getValueAt(r, 4));

        jButtonKlikLink.setText(
            linkTerpilih == null || "null".equalsIgnoreCase(linkTerpilih) || linkTerpilih.isBlank()
                ? "KLIK LINK DISINI" : linkTerpilih
        );

        boolean sudahDiterima = "DITERIMA".equalsIgnoreCase(status);
        jButtonDiterima.setEnabled(!sudahDiterima);
        jButtonDitolak.setEnabled(!sudahDiterima);
    }
    
    private void muatData(String q) {
        try {
            tblModel.setRowCount(0);
            var rows = sertifikatSvc.cariUntukVerifikasi(q);

            for (Object[] r : rows) {
                Integer id   = (Integer) r[0];
                Integer nik  = (Integer) r[1];
                String  nama = (String)  r[2];
                Integer idPel = (r[3] instanceof Number n) ? n.intValue() : null;
                String  stt  = (String)  r[4];
                String  lnk  = (String)  r[5];
                String  tgl  = asDateStr(r[6]);
                tblModel.addRow(new Object[]{ id, nik, nama, idPel, stt, lnk, tgl });
            }

            selectedId = null;
            linkTerpilih = "";
            jButtonKlikLink.setText("KLIK LINK DISINI");
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, "Gagal memuat: " + ex.getMessage(),
                    "Error", JOptionPane.ERROR_MESSAGE);
            logger.log(java.util.logging.Level.SEVERE, "Load verifikasi", ex);
        }
    }
    
    private void hubungPencarianLangsung() {
        jTextFieldPencarian.getDocument().addDocumentListener(new javax.swing.event.DocumentListener() {
            @Override 
            public void insertUpdate(DocumentEvent e) { 
                pencarianLangsung(); 
            }
            @Override 
            public void removeUpdate(DocumentEvent e) {
                pencarianLangsung(); 
            }
            @Override 
            public void changedUpdate(DocumentEvent e){
                pencarianLangsung(); 
            }
        });
    }
    private void pencarianLangsung() {
        String q = jTextFieldPencarian.getText().trim();
        muatData(q.isEmpty() ? null : q);
    }
    
    private static String asDateStr(Object o){
        if (o == null) 
            return "-";
        if (o instanceof java.time.LocalDate d)
            return d.toString();
        if (o instanceof java.sql.Date d)     
            return d.toLocalDate().toString();
        return String.valueOf(o);
    }
    private void bukaLink(String link){
        if (link == null || link.isBlank() || "null".equalsIgnoreCase(link)) {
            JOptionPane.showMessageDialog(this, "Tidak ada link untuk dibuka."); 
            return;
        }
        String url = (link.startsWith("http://") || link.startsWith("https://")) ? link : "https://" + link;
        try { java.awt.Desktop.getDesktop().browse(new java.net.URI(url)); }
        catch (Exception ex) {
            JOptionPane.showMessageDialog(this, "Gagal membuka: " + url, "Error", JOptionPane.ERROR_MESSAGE);
        }
    }
    
    private void hideColumn(int idx){
        var c = jTableSertifikat.getColumnModel().getColumn(idx);
        c.setMinWidth(0); c.setMaxWidth(0); c.setPreferredWidth(0);
    }

    
