    private void jButtonDiterimaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonDiterimaActionPerformed
        int r = jTableSertifikat.getSelectedRow();
        if (r < 0 || selectedId == null) {
            JOptionPane.showMessageDialog(this, "Pilih data di tabel.");
            return;
        }
        String st = String.valueOf(tblModel.getValueAt(r, 4));
        if ("DITERIMA".equalsIgnoreCase(st)) {
            JOptionPane.showMessageDialog(this, "Status sudah DITERIMA dan tidak dapat diubah.");
            return;
        }
        int ok = JOptionPane.showConfirmDialog(this,
                "Tetapkan DITERIMA? Insentif Rp3.500.000 akan dibuat.",
                "Konfirmasi", JOptionPane.YES_NO_OPTION);
        if (ok != JOptionPane.YES_OPTION) return;

        try {
            sertifikatSvc.verifikasi(selectedId, true, null);
            JOptionPane.showMessageDialog(this, "Berhasil diset DITERIMA. Insentif dibuat.");
            pencarianLangsung();
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
            logger.log(java.util.logging.Level.SEVERE, "Diterima", ex);
        }
    }//GEN-LAST:event_jButtonDiterimaActionPerformed

