    private void initTable() {
        tblModel = new javax.swing.table.DefaultTableModel(
            new Object[]{"ID", "Bidang", "Alasan", "Tanggal", "Status"}, 0
        ) {
            @Override 
            public boolean isCellEditable(int r, int c) { 
                return false; 
            }
        };
        jTablePengajuan.setModel(tblModel);
    }

    private void simpanPengajuan() {
        try {
            if (pengajuanSvc.hasPendingByNik(penggunaLogin.getNik())) {
                javax.swing.JOptionPane.showMessageDialog(this,
                    "Masih ada pengajuan MENUNGGU. Batalkan dulu.");
                return;
            }
            var pb = new model.PengajuanBantuan();
            pb.setPengguna(penggunaLogin);
            pb.setBidangDiminati(jTextFieldBidang.getText().trim());
            pb.setAlasanPengajuan(jTextFieldAlasan.getText().trim());
            pb.setStatusPengajuan("MENUNGGU");
            pb.setTanggalPengajuan(java.time.LocalDateTime.now());

            pengajuanSvc.tambah(pb);
            javax.swing.JOptionPane.showMessageDialog(this, "Pengajuan dikirim.");
            muatData();
            refreshTombol();
        } catch (Exception ex) {
            javax.swing.JOptionPane.showMessageDialog(this, ex.getMessage(), "Error",
                javax.swing.JOptionPane.ERROR_MESSAGE);
        }
    }

    private Integer pendingId;

    private void muatData() {
        tblModel.setRowCount(0);
        pendingId = null;

        var list = pengajuanSvc.temukanSemua();
        for (var x : list) {
            if (x.getPengguna() == null) 
                continue;
            if (!x.getPengguna().getNik().equals(penggunaLogin.getNik())) 
                continue;

            String tgl = x.getTanggalPengajuan() == null ? "-" : x.getTanggalPengajuan().toString();
            tblModel.addRow(new Object[]{
                x.getIdPengajuan(),
                x.getBidangDiminati(),
                x.getAlasanPengajuan(),
                tgl,
                x.getStatusPengajuan()
            });

            if ("MENUNGGU".equalsIgnoreCase(x.getStatusPengajuan())) {
                pendingId = x.getIdPengajuan();
            }
        }

        if (jTablePengajuan.getRowCount() > 0) {
            jTablePengajuan.setRowSelectionInterval(0, 0);
        } else {
            jTextFieldBidang.setText("");
            jTextFieldAlasan.setText("");
            jTextFieldTanggal.setText(LocalDate.now().toString());
            selectedId = null;
        }
    }

    private void refreshTombol() {
        boolean adaPending = pengajuanSvc.hasPendingByNik(penggunaLogin.getNik());
        jButtonOK.setEnabled(!adaPending);
        jButtonBatalkan.setEnabled(adaPending);
    }
    

