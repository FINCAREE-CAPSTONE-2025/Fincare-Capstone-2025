    private void initTablePelatihan() {
        tblModel = new javax.swing.table.DefaultTableModel(
            new Object[]{"ID", "Judul", "Lembaga", "Periode", "Harga"}, 0
        ){
            @Override 
            public boolean isCellEditable(int r, int c) { 
                return false; 
            }
        };
        jTable1.setModel(tblModel);
        jTable1.getTableHeader().setReorderingAllowed(false);
    }

    private String rupiah(Integer v){
        int x = (v == null ? 0 : v);
        return ("Rp " + String.format("%,d", x)).replace(',', '.');
    }

    private void pasangListenerRow() {
        jTable1.getSelectionModel().addListSelectionListener(e -> {
            if (!e.getValueIsAdjusting() && jTable1.getSelectedRow() >= 0) {
                int row = jTable1.getSelectedRow();
                jTextFieldJudul.setText(jTable1.getValueAt(row, 1).toString());
                jTextFieldLembaga.setText(jTable1.getValueAt(row, 2).toString());
                jTextFieldPeriode.setText(jTable1.getValueAt(row, 3).toString());
                jTextFieldHarga.setText(jTable1.getValueAt(row, 4).toString());
            }
        });
    }
    
    private void muatData(String q) {
        try {
            tblModel.setRowCount(0);
            var list = pelatihanSvc.temukanSemua();

            String low = (q == null) ? "" : q.trim().toLowerCase();
            boolean doFilter = !low.isEmpty();
            
            var df = (java.text.DecimalFormat)
                    java.text.NumberFormat.getCurrencyInstance(new java.util.Locale("id","ID"));
            df.setMaximumFractionDigits(0);

            for (var p : list) {
                String judul   = p.getJudul()   == null ? "" : p.getJudul();
                String lembaga = p.getLembaga() == null ? "" : p.getLembaga();
                String periode = (p.getPeriode() == null)
                        ? "" 
                        : p.getPeriode().format(DF);

                if (doFilter) {
                    boolean match = judul.toLowerCase().contains(low)
                                 || lembaga.toLowerCase().contains(low)
                                 || periode.toLowerCase().contains(low);
                    if (!match) 
                        continue;
                }

                tblModel.addRow(new Object[] {
                    p.getIdPelatihan(),
                    judul,
                    lembaga,
                    periode,
                    df.format(p.getHarga())
                });
            }

        } catch (Exception ex) {
            JOptionPane.showMessageDialog(
                this,
                "Gagal memuat data: " + ex.getMessage(),
                "Error",
                JOptionPane.ERROR_MESSAGE
            );
        }
    }

    private void PencarianLangsung() {
        String q = jTextFieldPencarian.getText().trim();
        muatData(q.isEmpty() ? null : q);
    }

    private void HubungPencarianLangsung() {
        jTextFieldPencarian.getDocument().addDocumentListener(new javax.swing.event.DocumentListener() {
            @Override 
            public void insertUpdate(DocumentEvent e) {
                PencarianLangsung(); 
            }
            @Override 
            public void removeUpdate(DocumentEvent e) {
                PencarianLangsung();
            }
            @Override 
            public void changedUpdate(DocumentEvent e) {
                PencarianLangsung(); 
            }
        });
    }
    
    private static String formatRupiah(int angka) {
        var locale = new java.util.Locale("id", "ID");
        var fmt = (java.text.DecimalFormat) java.text.NumberFormat.getCurrencyInstance(locale);
        fmt.setMaximumFractionDigits(0);
        return fmt.format(angka);
    }

