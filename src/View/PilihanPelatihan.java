    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        final int nik = penggunaLogin.getNik();

        if (!pelatihanSvc.bolehBeliPelatihan(nik)) {
            JOptionPane.showMessageDialog(
                this,
                "Pengajuan belum DITERIMA. Tidak bisa membeli pelatihan.",
                "Tidak Bisa Membeli",
                JOptionPane.WARNING_MESSAGE
            );
            return;
        }
        
        int row = jTable1.getSelectedRow();
        if (row < 0) {
            JOptionPane.showMessageDialog(this, "Pilih dulu pelatihan di tabel.");
            return;
        }

        int idPel;
        String judul;
        int harga;
        try {
            idPel = (int) jTable1.getValueAt(row, 0);
            judul = String.valueOf(jTable1.getValueAt(row, 1));
            Object hargaObj = jTable1.getValueAt(row, 4);
            harga = (hargaObj instanceof Number) ? ((Number) hargaObj).intValue()
                                                 : Integer.parseInt(String.valueOf(hargaObj));
        } catch (Exception parseEx) {
            JOptionPane.showMessageDialog(this, "Struktur tabel tidak sesuai: " + parseEx.getMessage(),
                    "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        try {
            if (pelatihanSvc.sudahPernahBeli(nik, idPel)) {
                JOptionPane.showMessageDialog(
                    this,
                    "Pelatihan \"" + judul + "\" sudah pernah kamu beli.",
                    "Informasi",
                    JOptionPane.INFORMATION_MESSAGE
                );
                return;
            }
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, "Gagal cek status pembelian: " + ex.getMessage(),
                    "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        int ok = JOptionPane.showConfirmDialog(
            this,
            "Beli \"" + judul + "\" seharga " + formatRupiah(harga) + " ?",
            "Konfirmasi Pembelian",
            JOptionPane.YES_NO_OPTION
        );
        if (ok != JOptionPane.YES_OPTION) 
            return;

        try {
            pelatihanSvc.beliPelatihan(nik, idPel);
            JOptionPane.showMessageDialog(this, "Pembelian berhasil. Saldo terpotong.", "Sukses",
                    JOptionPane.INFORMATION_MESSAGE);

            muatData(null);
            JOptionPane.showMessageDialog(this, 
                    "Silakan cek riwayat pada menu Profil.", 
                    "Sukses", JOptionPane.INFORMATION_MESSAGE);
            
        } catch (IllegalStateException ex) {
 
            String msg = ex.getMessage();
            if ("INSUFFICIENT".equalsIgnoreCase(msg)) {
                JOptionPane.showMessageDialog(this,
                        "Saldo tidak cukup untuk membeli pelatihan ini.",
                        "Saldo Tidak Cukup",
                        JOptionPane.WARNING_MESSAGE);
            } else if (msg != null && msg.toLowerCase().contains("sudah")) {
                JOptionPane.showMessageDialog(this, msg, "Informasi", JOptionPane.INFORMATION_MESSAGE);
            } else {
                JOptionPane.showMessageDialog(this, msg, "Gagal", JOptionPane.ERROR_MESSAGE);
            }
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, "Terjadi kesalahan: " + ex.getMessage(),
                    "Error", JOptionPane.ERROR_MESSAGE);
            ex.printStackTrace();
        }
    }//GEN-LAST:event_jButton1ActionPerformed

