    private void jButtonMasukActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonMasukActionPerformed
        String email = jTextField1.getText().trim();
        String password = new String(jPasswordField1.getPassword());

        if (email.isEmpty() || password.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Email dan password wajib diisi.", 
                    "Validasi", JOptionPane.WARNING_MESSAGE);
            return;
        }

        try {
            Akun akun = authService.login(email, password);

            if (akun == null) {
                JOptionPane.showMessageDialog(this, "Email atau Password salah!", 
                        "Login Gagal", JOptionPane.ERROR_MESSAGE);
                return;
            }

            if (akun.getPeran() != null) {
                if ("admin".equalsIgnoreCase(akun.getPeran())) {
                    JOptionPane.showMessageDialog(this, 
                            "Halo Admin " + akun.getNama() + " ðŸ‘‘",
                            "Selamat Datang", JOptionPane.INFORMATION_MESSAGE);
                } else {
                    JOptionPane.showMessageDialog(this, 
                            "Hai " + akun.getNama() + ", selamat datang di Fincare! ðŸŒ¼",
                            "Selamat Datang", JOptionPane.INFORMATION_MESSAGE);
                }
            }
            
            if ("admin".equalsIgnoreCase(akun.getPeran())) {
                new View.HomeAdmin(akun).setVisible(true);
            } else {
                new View.HomePengguna(akun).setVisible(true);
            }

            this.dispose();

        } catch (Exception e) {
            logger.log(java.util.logging.Level.SEVERE, "Login error", e);
            JOptionPane.showMessageDialog(this, 
                    "Terjadi kesalahan: " + e.getMessage(), 
                    "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButtonMasukActionPerformed

